public class Class{
  private String name;
  private int number;
  private String desc;

  public String getName()
  {
    return name;
  }
  public void setName(String name)
  {
    this.name = name;
  }
  public int getNumber()
  {
    return number;
  }
  public void setNumber(int number)
  {
    this.number = number;
  }
  public String getDesc()
  {
    return desc;
  }
  public void setDesc(String desc)
  {
    this.desc = desc;
  }
}